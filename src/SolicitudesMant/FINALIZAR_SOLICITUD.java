    public void actualizarServicio(){
    
       try {
            Connection con = Conn.getConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("update incidencias set estatus = "+estatus+",fecha_inicio ='"+fechainicion+"',notas = '"+NOTAFIN.getText()+"'  where no_incidencias ="+idincidencia);
            rs.close();
            stmt.close();
            con.close();
        } catch (SQLException error) {
            System.out.print(error + " ERROR QUE OBTIENE EL ULTIMO ID DE INGRESO ");
        }
    }
    
   public void actualizarServicioFin(){
    
       try {
            Connection con = Conn.getConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("update incidencias set estatus = "+estatus+",fecha_cerrado ='"+fechafin+"',estado = 2,notas = '"+NOTAFIN.getText()+"'  where no_incidencias ="+idincidencia);
            rs.close();
            stmt.close();
            con.close();
        } catch (SQLException error) {
            System.out.print(error + " ERROR QUE OBTIENE EL ULTIMO ID DE INGRESO ");
        }   
    
    }
    
