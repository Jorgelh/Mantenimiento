    private void AGREGARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AGREGARActionPerformed

        if (NOTAFIN.getText().compareTo("") != 0 && !ESTATUS.getSelectedItem().toString().equalsIgnoreCase("SELECCIONAR...")) {
            
            if (ESTATUS.getSelectedItem().toString().equalsIgnoreCase("ESPERANDO REPUESTOS")) {
                estatus = 3;
                Date date = fecha.getDate();
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                fechainicion = sdf.format(date);
                actualizarServicio();
                ListaSolicitudes();
                limpiar();
                
            } else if (ESTATUS.getSelectedItem().toString().equalsIgnoreCase("EN PROCESO")) {
                estatus=2;
                Date date = fecha.getDate();
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                fechainicion = sdf.format(date);
                actualizarServicio();
                ListaSolicitudes();
                limpiar();
            } else if (ESTATUS.getSelectedItem().toString().equalsIgnoreCase("TERMINADA")) {
                estatus = 4;
                Date date = fecha.getDate();
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                fechafin = sdf.format(date);
                actualizarServicioFin();
                ListaSolicitudes();
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(null, "LLENE TODOS LOS CAMPOS");
        }
    }//GEN-LAST:event_AGREGARActionPerformed
