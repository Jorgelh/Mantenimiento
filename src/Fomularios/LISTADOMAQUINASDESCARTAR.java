    private void ListaMaquinas() {
        ArrayList<maquinas> result1 = Ingreso_Mantenimiento.ListadoGeneralMaquinas(CODIGO.getText());
        recargarTable(result1);
    }
    
    public void recargarTable(ArrayList<maquinas> list) {
        Object[][] datos1 = new Object[list.size()][5];
        int i = 0;
        for (maquinas p : list)
        {
            datos1[i][0] = p.getNO();
            datos1[i][1] = p.getDESCRIPCION();
            datos1[i][2] = p.getFABRICANTE();
            datos1[i][3] = p.getSERIE();
            datos1[i][4] = p.getFUNCIONAMIENTO();
            i++;
        }
        maquinas.setModel(new javax.swing.table.DefaultTableModel(
                datos1,
                new String[]{
                    "CODIGO","DESCRIPCION","FABRICANTE","SERIE","ESTADO"
                }) {
                     @Override
        public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
        
        TableColumn columna1 = maquinas.getColumn("CODIGO");
        columna1.setPreferredWidth(0);
        TableColumn columna2 = maquinas.getColumn("DESCRIPCION");
        columna2.setPreferredWidth(350);
    }
