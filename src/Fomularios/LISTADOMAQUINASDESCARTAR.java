    private void GUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GUARDARActionPerformed
           if(NOTA.getText().compareTo("")!=0&& CODIGO2.getText().compareTo("")!=0 && FECHA.getDate() != null)       
         try {
            Connection con = Conn.getConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("update MAQUINARIA set estado = 2,NOTA = '"+NOTA.getText()+"',FECHADESCARTE = '"+new java.sql.Date(FECHA.getDate().getTime())+"' where no ="+CODIGO2.getText());
            rs.close();
            stmt.close();
            con.close();
            JOptionPane.showMessageDialog(null, "MAQUINA SE A ELIMINADO DEL SISTEMA");
            limpiar();
            ListaMaquinas();
        } catch (SQLException error) {
            System.out.print(error+" ERROR QUE HACE UPDATE");
        }
           else {JOptionPane.showMessageDialog(null, "LLENAR TODOS LOS CAMPOS...");}
    }//GEN-LAST:event_GUARDARActionPerformed

