    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        if (INICIO.getDate() != null && FIN.getDate() != null) {

            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yy");
            Date fec1 = INICIO.getDate();
            Date fec2 = FIN.getDate();
            String fe1 = formato.format(fec1);
            String fe2 = formato.format(fec2);
            try {
                Date f1 = formato.parse(fe1);
                Date f2 = formato.parse(fe2);

                if (f1.before(f2)) {
                  ListaIndicadores();
                  promedioIndicadores();

                } else {
                    JOptionPane.showMessageDialog(null, "LA PRIMERA FECHA NO PUEDE SER MAYOR A LA SEGUNDA FECHA");
                }

            } catch (ParseException ex) {
                Logger.getLogger(LISTARFECHA.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "INGRESE LAS FECHAS...");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
    
    private void ListaIndicadores() {
        
        Date date = INICIO.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yy");
        String fecha = sdf.format(date);
        
        Date date2 = FIN.getDate();
        SimpleDateFormat sd = new SimpleDateFormat("dd/MM/yy");
        String fecha1 = sd.format(date2);
        
        ArrayList<manto> result1 = Ingreso_Mantenimiento.ListarIndicadores(fecha,fecha1);
        recargarTable(result1);
    }
    
    public void recargarTable(ArrayList<manto> list) {
        Object[][] datos1 = new Object[list.size()][5];
        int i = 0;
        for (manto p : list)
        {
            datos1[i][0] = p.getCodigo();
            datos1[i][1] = p.getDescripcion();
            datos1[i][2] = p.getFechaIdeal();
            datos1[i][3] = p.getRealizacion();
            datos1[i][4] = p.getCumplimiento();
            
            i++;
        }
        Indicadores.setModel(new javax.swing.table.DefaultTableModel(
                datos1,
                new String[]{
                    "CODIGO","DESCRIPCION","FECHA","TIEMPO REALIZACION","CUMPLIMIENTO"
                }) {
                     @Override
        public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
        
        TableColumn columna1 = Indicadores.getColumn("CODIGO");
        columna1.setPreferredWidth(0);
        TableColumn columna2 = Indicadores.getColumn("DESCRIPCION");
        columna2.setPreferredWidth(200);
        
    }
    
    
    
    
