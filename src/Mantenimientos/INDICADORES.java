    public void promedioIndicadores()
     {
         Date date = INICIO.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yy");
        String fecha = sdf.format(date);
        
        Date date2 = FIN.getDate();
        SimpleDateFormat sd = new SimpleDateFormat("dd/MM/yy");
        String fecha1 = sd.format(date2);
         
         try {
            Connection con = Conn.getConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select ROUND(SUM(tiempo_realizacion)/COUNT(tiempo_realizacion)) as \"TIEMPO REALIZACION\",ROUND(sum(cumplimiento)/COUNT(cumplimiento)) AS CUMPLIMIENTO from indicadores where fecha BETWEEN TO_DATE('"+fecha+"','dd/mm/yy') and TO_DATE('"+fecha1+"','dd/mm/yy')");
            while (rs.next()) {
                int lastID = rs.getInt(1);
                realizacion=lastID;
                cumplimiento = rs.getInt(2);
                
            }
            rs.close();
            stmt.close();
            con.close();
        } catch (SQLException error) {
            System.out.print(error+" ERROR QUE OBTIENE EL ULTIMO ID DE INGRESO ");
        }
             
             if(realizacion>=0){TIEMPOREA.setForeground(Color.BLUE);}else{TIEMPOREA.setForeground(Color.RED);}
             if(cumplimiento>=0){CUMPLIPROGRAMA.setForeground(Color.BLUE);}else{CUMPLIPROGRAMA.setForeground(Color.RED);}
             TIEMPOREA.setText(String.valueOf(realizacion ));
             CUMPLIPROGRAMA.setText(String.valueOf(cumplimiento));
     }
    
    
